// 11.3 Concat Method
// this method combines 2 or more arrays
var bakery1 = ['muffin', 'croissant', 'cookie']
var bakery2 = ['bagel', 'brownie']

var newBakery = bakery1.concat(bakery2);
console.log(newBakery);     // this logs an array of all 5 items with bakery2 items adding to the end of the list